export function useI18n() {
  const locale = localStorage.getItem('claw-office-locale') || 'zh'

  function t(key, params = {}) {
    const msg = messages[locale]?.[key] || messages.en[key] || key
    return msg.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? '')
  }

  function setLocale(newLocale) {
    localStorage.setItem('claw-office-locale', newLocale)
    window.location.reload()
  }

  return { locale, t, setLocale, availableLocales: ['zh', 'en', 'ja'] }
}
